package calculate

func Addition(input1, input2 int) int {
	result := input1 + input2
	return result
}

func Subtraction(input1, input2 int) int {
	result := input1 - input2
	return result
}
func Division(input1, input2 int) int {
	result := input1 / input2
	return result
}
func Multiplication(input1, input2 int) int {
	result := input1 * input2
	return result
}
